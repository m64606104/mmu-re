// 表情包Prompt构建工具

import { getAllAvailableStickers } from './stickerStorage';

/**
 * 构建表情包使用指南的系统提示
 */
export const buildStickerPrompt = async (characterId?: string): Promise<string> => {
  try {
    const allStickers = await getAllAvailableStickers(characterId);
    
    if (allStickers.length === 0) {
      return '';
    }

    // 🔥 智能采样：如果表情包超过30个，只展示最常用的30个
    const MAX_DISPLAY_STICKERS = 30;
    let stickers = allStickers;
    
    if (allStickers.length > MAX_DISPLAY_STICKERS) {
      // 按使用次数排序，取前30个最常用的
      const sorted = [...allStickers].sort((a, b) => (b.usage || 0) - (a.usage || 0));
      stickers = sorted.slice(0, MAX_DISPLAY_STICKERS);
      console.log(`📦 [表情包Prompt] 总共${allStickers.length}个，只展示最常用的${MAX_DISPLAY_STICKERS}个`);
    }

    // 按描述分组，避免重复
    const stickerDescriptions = stickers.map(s => s.description);
    
    const prompt = `
━━━━━━━━━━━━━━━━━━━━━━━━
📦 可用表情包列表
━━━━━━━━━━━━━━━━━━━━━━━━

你可以使用以下表情包来丰富对话表达：

${stickerDescriptions.map((desc, index) => `${index + 1}. ${desc}`).join('\n')}
${allStickers.length > MAX_DISPLAY_STICKERS ? `\n💡 提示：以上只展示最常用的${MAX_DISPLAY_STICKERS}个表情包，实际还有更多。如果列表中没有你想要的，可以用关键词（如"开心"、"伤心"），系统会智能匹配相关表情包。` : ''}

━━━━━━━━━━━━━━━━━━━━━━━━
💡 使用规则
━━━━━━━━━━━━━━━━━━━━━━━━

1. **智能选择流程**（两步思考法）：
   
   **第一步：确定情绪关键词**
   - 先在心里想：我现在要表达什么情绪？（如"开心"、"伤心"、"疑惑"、"鼓励"等）
   
   **第二步：从列表中筛选**
   - 在上面的表情包列表中，找到所有包含这个关键词的表情包
   - 例如要表达"伤心"，列表中可能有：
     * 小狗哭泣
     * 小狗流泪
     * 小狗大哭
     * 委屈哭泣
   
   **第三步：精确选择**
   - 根据**对话语境的强弱**和**你的角色性格**，选择最合适的那个
   - 语境很悲伤 → 选"小狗大哭"
   - 语境轻微难过 → 选"小狗流泪"
   - 觉得委屈 → 选"委屈哭泣"
   
   **发送格式**：\`[表情包:完整描述]\`
   - ✅ 正确：\`[表情包:小狗流泪]\`（完整照抄列表中的描述）
   - ❌ 错误：\`[表情包:伤心]\`（太笼统，应该选具体的那个）

2. **使用时机**：
   - 💬 在适合的场景下自然使用表情包
   - 😊 可以单独发送表情包，也可以配合文字
   - 🎯 根据对话情绪和氛围选择合适的表情包
   - ✍️ 默认先考虑自然文字；只有文字不够表达时再用表情包增强语气

3. **优先级原则**：
   - ⭐ **最优先**：使用上面列表中的真实表情包
   - 📋 仔细查看表情包列表，找到最匹配的表情包
   - ✅ 当你决定使用表情包时，使用 \`[表情包:描述]\` 格式
   - ✅ 如果当前更适合纯文字，就直接发文字，不需要强行发表情包
   - ❌ **绝对禁止**编造列表中不存在的表情包
   - ❌ **绝对禁止**修改列表中的描述文字

4. **使用频率**：
   - ⚡ 不要机械频率控制
   - 🎨 根据角色性格与表达习惯自然决定（外向活泼可更常用，克制理性可更少用）
   - 💡 根据上下文选择时机：情绪强化、调节气氛、幽默回应时更适合
   - 🤝 保持对话自然感，避免“为了发而发”

5. **描述匹配**：
   - 🔍 根据表情包的描述判断是否适合当前场景
   - 🎯 例如"小猫疑问"适合表达困惑、不理解时使用
   - 💪 例如"加油打气"适合鼓励、支持时使用
   - 🎭 如果有多个类似的表情包，选择最符合当前情绪的

6. **示例用法**：
   \`\`\`
   用户：我今天数学考试没考好...
   你：别灰心啦！[表情包:加油打气] 下次肯定能考好的！
   
   用户：你能帮我解释一下这个问题吗？
   你：[表情包:小猫疑问] 你是说这个部分吗？
   
   用户：太感谢你了！
   你：不客气呀~ [表情包:开心微笑]
   \`\`\`

⚠️ 重要：
- 只使用上面列表中的表情包描述
- 不要编造不存在的表情包
- 保持自然，不要过度使用
- 大多数回复可以只用文字，不需要为了“看起来活泼”强行发表情包

━━━━━━━━━━━━━━━━━━━━━━━━
`;

    return prompt;
  } catch (error) {
    console.error('Failed to build sticker prompt:', error);
    return '';
  }
};
