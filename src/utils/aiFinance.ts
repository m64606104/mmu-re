/**
 * 获取交易历史（可选分类和日期范围筛选）
 */
export const getTransactionHistory = async (
  aiId: string,
  filters?: {
    type?: 'income' | 'expense';
    category?: string;
    startDate?: number;
    endDate?: number;
    limit?: number;
  }
): Promise<FinanceTransaction[]> => {
  const data = await getAIFinanceData(aiId);
  let transactions = [...data.transactions];
  
  // 应用筛选
  if (filters) {
    if (filters.type) {
      transactions = transactions.filter(t => t.type === filters.type);
    }
    if (filters.category) {
      transactions = transactions.filter(t => t.category === filters.category);
    }
    if (filters.startDate) {
      transactions = transactions.filter(t => t.timestamp >= filters.startDate!);
    }
    if (filters.endDate) {
      transactions = transactions.filter(t => t.timestamp <= filters.endDate!);
    }
    if (filters.limit) {
      transactions = transactions.slice(0, filters.limit);
    }
  }
  
  return transactions;
};

/**
 * 获取财务统计
 */
export const getFinanceStats = async (aiId: string): Promise<{
  balance: number;
  totalIncome: number;
  totalExpense: number;
  thisMonthIncome: number;
  thisMonthExpense: number;
  todayTransactionCount: number;
}> => {
  const data = await getAIFinanceData(aiId);
  const now = new Date();
  const monthStart = new Date(now.getFullYear(), now.getMonth(), 1).getTime();
  const todayStart = new Date(now.getFullYear(), now.getMonth(), now.getDate()).getTime();
  
  const thisMonthTransactions = data.transactions.filter(t => t.timestamp >= monthStart);
  const todayTransactions = data.transactions.filter(t => t.timestamp >= todayStart);
  
  return {
    balance: data.balance,
    totalIncome: data.totalIncome,
    totalExpense: data.totalExpense,
    thisMonthIncome: thisMonthTransactions
      .filter(t => t.type === 'income')
      .reduce((sum, t) => sum + t.amount, 0),
    thisMonthExpense: thisMonthTransactions
      .filter(t => t.type === 'expense')
      .reduce((sum, t) => sum + t.amount, 0),
    todayTransactionCount: todayTransactions.length
  };
};


