import { AIFinanceData, FinanceTransaction, IncomeConfig } from '../types';
import { smartLoad, smartSave } from './storage';

const FINANCE_STORAGE_KEY = 'ai_finance_data';

/**
 * 获取所有AI的财务数据
 */
const getAllFinanceData = async (): Promise<AIFinanceData[]> => {
  try {
    const data = await smartLoad(FINANCE_STORAGE_KEY);
    return data || [];
  } catch (error) {
    console.error('加载财务数据失败:', error);
    return [];
  }
};

/**
 * 保存财务数据
 */
const saveAllFinanceData = async (data: AIFinanceData[]): Promise<void> => {
  try {
    await smartSave(FINANCE_STORAGE_KEY, data);
  } catch (error) {
    console.error('保存财务数据失败:', error);
  }
};

/**
 * 根据AI角色智能生成初始资金
 */
const generateInitialBalance = (characterSettings?: any): number => {
  if (!characterSettings) return 5000; // 默认5000
  
  const personality = (characterSettings.personality || '').toLowerCase();
  const background = (characterSettings.background || '').toLowerCase();
  const combined = `${personality} ${background}`.toLowerCase();
  
  // 高级职业/身份 - 10000-20000
  if (combined.includes('总裁') || combined.includes('ceo') || combined.includes('董事长') ||
      combined.includes('老板') || combined.includes('企业家') || combined.includes('投资') ||
      combined.includes('富豪') || combined.includes('千金') || combined.includes('公主')) {
    return Math.floor(Math.random() * 10000) + 10000; // 10000-20000
  }
  
  // 中高级职业 - 7000-12000  
  if (combined.includes('医生') || combined.includes('律师') || combined.includes('工程师') ||
      combined.includes('程序员') || combined.includes('设计师') || combined.includes('经理') ||
      combined.includes('主管') || combined.includes('教授')) {
    return Math.floor(Math.random() * 5000) + 7000; // 7000-12000
  }
  
  // 学生/新人 - 3000-6000
  if (combined.includes('学生') || combined.includes('实习') || combined.includes('新人') ||
      combined.includes('毕业') || combined.includes('校园')) {
    return Math.floor(Math.random() * 3000) + 3000; // 3000-6000
  }
  
  // 特殊身份 - 8000-15000
  if (combined.includes('网红') || combined.includes('主播') || combined.includes('明星') ||
      combined.includes('艺人') || combined.includes('模特')) {
    return Math.floor(Math.random() * 7000) + 8000; // 8000-15000
  }
  
  // 默认中等收入 - 5000-8000
  return Math.floor(Math.random() * 3000) + 5000;
};

/**
 * 获取指定AI的财务数据
 */
export const getAIFinanceData = async (aiId: string, characterSettings?: any): Promise<AIFinanceData> => {
  const allData = await getAllFinanceData();
  const existing = allData.find(d => d.aiId === aiId);
  
  if (existing) {
    return existing;
  }
  
  // 🎯 根据AI角色智能生成初始资金
  const initialBalance = generateInitialBalance(characterSettings);
  
  // 创建新的财务数据
  const newData: AIFinanceData = {
    aiId,
    balance: initialBalance,
    totalIncome: initialBalance,
    totalExpense: 0,
    transactions: [{
      id: `trans_${Date.now()}`,
      timestamp: Date.now(),
      type: 'income',
      amount: initialBalance,
      category: '初始资金',
      description: characterSettings ? 
        `根据${characterSettings.nickname || 'AI'}的身份背景自动配置的启动资金` : 
        '系统赠送的启动资金',
      autoGenerated: true
    }],
    incomeConfigs: [],
    settings: {
      allowNegativeBalance: false,
      notifyOnLowBalance: true,
      lowBalanceThreshold: Math.floor(initialBalance * 0.1) // 10%作为低余额阈值
    }
  };
  
  console.log(`💰 为AI ${characterSettings?.nickname || aiId} 生成初始资金: ¥${initialBalance}`);
  
  allData.push(newData);
  await saveAllFinanceData(allData);
  return newData;
};

/**
 * 更新AI财务数据
 */
const updateAIFinanceData = async (data: AIFinanceData): Promise<void> => {
  const allData = await getAllFinanceData();
  const index = allData.findIndex(d => d.aiId === data.aiId);
  
  if (index >= 0) {
    allData[index] = data;
  } else {
    allData.push(data);
  }
  
  await saveAllFinanceData(allData);
};

/**
 * 添加交易记录
 */
export const addTransaction = async (
  aiId: string,
  type: 'income' | 'expense',
  amount: number,
  category: string,
  description: string,
  relatedUserId?: string,
  relatedMessageId?: string,
  autoGenerated: boolean = false
): Promise<boolean> => {
  try {
    const financeData = await getAIFinanceData(aiId);
    
    // 检查支出是否超出余额
    if (type === 'expense' && !financeData.settings.allowNegativeBalance) {
      if (financeData.balance < amount) {
        console.warn(`AI ${aiId} 余额不足，无法完成支出`);
        return false;
      }
    }
    
    // 创建交易记录
    const transaction: FinanceTransaction = {
      id: `trans_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
      timestamp: Date.now(),
      type,
      amount,
      category,
      description,
      relatedUserId,
      relatedMessageId,
      autoGenerated
    };
    
    // 更新余额和总计
    if (type === 'income') {
      financeData.balance += amount;
      financeData.totalIncome += amount;
    } else {
      financeData.balance -= amount;
      financeData.totalExpense += amount;
    }
    
    // 添加交易记录
    financeData.transactions.unshift(transaction); // 最新的放在前面
    
    // 限制交易记录数量（保留最近1000条）
    if (financeData.transactions.length > 1000) {
      financeData.transactions = financeData.transactions.slice(0, 1000);
    }
    
    await updateAIFinanceData(financeData);
    
    console.log(`✅ AI ${aiId} ${type === 'income' ? '收入' : '支出'} ¥${amount}，当前余额: ¥${financeData.balance}`);
    return true;
  } catch (error) {
    console.error('添加交易记录失败:', error);
    return false;
  }
};

/**
 * 添加收入配置
 */
export const addIncomeConfig = async (
  aiId: string,
  config: Omit<IncomeConfig, 'lastIncomeTime' | 'nextIncomeTime'>
): Promise<void> => {
  const financeData = await getAIFinanceData(aiId);
  
  const now = Date.now();
  const newConfig: IncomeConfig = {
    ...config,
    lastIncomeTime: 0,
    nextIncomeTime: now + calculateNextIncomeDelay(config.frequency)
  };
  
  financeData.incomeConfigs.push(newConfig);
  await updateAIFinanceData(financeData);
};

/**
 * 更新收入配置
 */
export const updateIncomeConfig = async (
  aiId: string,
  configIndex: number,
  updates: Partial<IncomeConfig>
): Promise<void> => {
  const financeData = await getAIFinanceData(aiId);
  
  if (configIndex >= 0 && configIndex < financeData.incomeConfigs.length) {
    financeData.incomeConfigs[configIndex] = {
      ...financeData.incomeConfigs[configIndex],
      ...updates
    };
    await updateAIFinanceData(financeData);
  }
};

/**
 * 删除收入配置
 */
export const removeIncomeConfig = async (
  aiId: string,
  configIndex: number
): Promise<void> => {
  const financeData = await getAIFinanceData(aiId);
  financeData.incomeConfigs.splice(configIndex, 1);
  await updateAIFinanceData(financeData);
};

/**
 * 计算下次收入延迟（毫秒）
 */
const calculateNextIncomeDelay = (frequency: IncomeConfig['frequency']): number => {
  switch (frequency) {
    case 'daily':
      return 24 * 60 * 60 * 1000; // 1天
    case 'weekly':
      return 7 * 24 * 60 * 60 * 1000; // 7天
    case 'monthly':
      return 30 * 24 * 60 * 60 * 1000; // 30天
    case 'random':
      // 随机1-7天
      const days = 1 + Math.random() * 6;
      return days * 24 * 60 * 60 * 1000;
    default:
      return 24 * 60 * 60 * 1000;
  }
};

/**
 * 处理自动收入（在App启动或定时检查时调用）
 */
export const processAutoIncome = async (aiId: string): Promise<void> => {
  try {
    const financeData = await getAIFinanceData(aiId);
    const now = Date.now();
    let hasUpdate = false;
    
    for (let i = 0; i < financeData.incomeConfigs.length; i++) {
      const config = financeData.incomeConfigs[i];
      
      if (!config.enabled) continue;
      
      // 检查是否到了收入时间
      if (now >= config.nextIncomeTime) {
        // 计算实际收入金额
        let amount = config.baseAmount;
        if (config.randomRange) {
          const [min, max] = config.randomRange;
          amount = min + Math.random() * (max - min);
          amount = Math.round(amount * 100) / 100; // 保留两位小数
        }
        
        // 添加收入记录
        await addTransaction(
          aiId,
          'income',
          amount,
          config.description,
          `${config.description} - ${new Date().toLocaleDateString()}`,
          undefined,
          undefined,
          true
        );
        
        // 更新下次收入时间
        config.lastIncomeTime = now;
        config.nextIncomeTime = now + calculateNextIncomeDelay(config.frequency);
        hasUpdate = true;
      }
    }
    
    if (hasUpdate) {
      await updateAIFinanceData(financeData);
    }
  } catch (error) {
    console.error(`处理AI ${aiId} 自动收入失败:`, error);
  }
};

