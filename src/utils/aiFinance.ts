/**
 * 为已存在的AI升级智能财务系统
 */
export const upgradeExistingAIFinance = async (
  aiId: string, 
  characterSettings: any
): Promise<boolean> => {
  try {
    const allData = await getAllFinanceData();
    const existingIndex = allData.findIndex(d => d.aiId === aiId);
    
    if (existingIndex === -1) {
      return false; // 不存在，无需升级
    }
    
    const existing = allData[existingIndex];
    
    // 检查是否需要升级（判断是否是默认的1000余额或者缺少角色背景）
    const isDefaultBalance = existing.balance === 1000 || existing.balance === 5000;
    const lacksCharacterDescription = existing.transactions.length === 1 && 
      existing.transactions[0].description === '系统赠送的启动资金';
    
    if (isDefaultBalance || lacksCharacterDescription) {
      // 需要升级：重新生成智能初始资金
      const smartBalance = generateInitialBalance(characterSettings);
      const balanceDiff = smartBalance - existing.balance;
      
      console.log(`🔄 升级AI ${characterSettings?.nickname || aiId} 财务系统: ${existing.balance} → ${smartBalance}`);
      
      // 更新余额和总收入
      existing.balance = smartBalance;
      existing.totalIncome += balanceDiff;
      
      // 添加升级记录
      existing.transactions.unshift({
        id: `upgrade_${Date.now()}`,
        timestamp: Date.now(),
        type: 'income',
        amount: balanceDiff,
        category: '系统升级',
        description: `智能财务系统升级：根据${characterSettings?.nickname || 'AI'}的身份背景重新配置资金`,
        autoGenerated: true
      });
      
      // 更新低余额阈值
      existing.settings.lowBalanceThreshold = Math.floor(smartBalance * 0.1);
      
      allData[existingIndex] = existing;
      await saveAllFinanceData(allData);
      
      return true;
    }
    
    return false; // 无需升级
  } catch (error) {
    console.error('升级AI财务系统失败:', error);
    return false;
  }
};

