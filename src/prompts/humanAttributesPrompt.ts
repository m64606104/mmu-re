import { GrowthStage } from '../types';

/**
 * 根据成长阶段获取大致年龄
 */
function getAgeFromStage(stage: GrowthStage): number {
  switch (stage) {
    case 'baby': return 2;
    case 'toddler': return 5;
    case 'child': return 10;
    case 'teen': return 16;
    default: return 10;
  }
}

